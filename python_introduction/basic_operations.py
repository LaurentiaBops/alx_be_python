#Performing basic arithmetic operations

#Define two variables
number1 = 10
number2 = 5

#Calculate sum of the two variables
sum = number1 + number2
print("Addition of", str(number1), "and", str(number2), 
"is", str(sum))

#Calculate difference of the two variables
difference = number1 - number2
print("Subtraction of", str(number1), "and", str(number2), 
"is", str(difference))

#Calculate product of the two variables
multipication = number1 * number2
print("Multiplication of", str(number1), "and", str(number2), 
"is", str(multipication))